# 模块说明
# ============================================================================

"""
FCA-CL 模块包含两个核心组件:

1. 跨层特征聚合 (Cross-layer Feature Aggregation)
   - 融合 YOLOv8 特征金字塔的 P2 和 P3 层
   - P2: 浅层高分辨率特征（保留小目标细节）
   - P3: 深层语义特征（提供上下文信息）
   - 通过可学习权重 α, β 自适应融合
   - 公式: F_small = α·P2_conv + β·Up(P3)

2. 对比学习增强 (Contrastive Learning Enhancement)
   - 使用 InfoNCE 损失函数
   - 拉近同类小目标特征距离
   - 推远小目标与背景/雾噪声的距离
   - 提升小目标和背景的区分度

优势:
✓ 参数量增加 < 2%
✓ 计算量增加 < 8%
✓ 即插即用，无需修改训练流程
✓ 显著提升雾天小目标检测精度
"""

